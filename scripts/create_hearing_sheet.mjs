import { spawnSync } from 'child_process';
import { writeFileSync } from 'fs';
import { join } from 'path';

// gws の Node.js エントリーポイントを直接実行（cmd.exe を迂回）
const GWS_JS = join(process.env.APPDATA, 'npm', 'node_modules', '@googleworkspace', 'cli', 'run-gws.js');

function runGws(args) {
  const r = spawnSync(process.execPath, [GWS_JS, ...args], {
    encoding: 'utf8', maxBuffer: 8 * 1024 * 1024, shell: false, windowsHide: true,
  });
  if (r.error) throw r.error;
  if (r.status !== 0) throw new Error(r.stderr || r.stdout || `exit ${r.status}`);
  return r.stdout;
}

// ── スプレッドシート作成 ─────────────────────────────────────────
function createSpreadsheet() {
  const body = JSON.stringify({
    properties: { title: '藤井さんヒアリングワークシート_2026-04-02' },
    sheets: [
      { properties: { title: 'STEP1_要素分解', index: 0 } },
      { properties: { title: 'STEP2_教師MTGアジェンダ', index: 1 } },
      { properties: { title: 'STEP3_レポートイメージ', index: 2 } },
      { properties: { title: 'STEP4_修正パターン', index: 3 } },
    ],
  });
  return JSON.parse(runGws(['sheets', 'spreadsheets', 'create', '--json', body]));
}

// ── 値の書き込み ─────────────────────────────────────────────────
function updateValues(ssId, sheetName, rows) {
  const params = JSON.stringify({ spreadsheetId: ssId, range: `${sheetName}!A1`, valueInputOption: 'USER_ENTERED' });
  const body = JSON.stringify({ values: rows });
  runGws(['sheets', 'spreadsheets', 'values', 'update', '--params', params, '--json', body]);
  console.log(`  ${sheetName} 書き込み完了`);
}

